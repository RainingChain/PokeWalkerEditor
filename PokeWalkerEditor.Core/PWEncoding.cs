    public static string GetString(ReadOnlySpan<byte> data)
    {
        var bytes = new byte[data.Length];
        var i = 0;
        for (; true; i++)
            {
            var val = ReadUInt16LittleEndian(data.Slice(i * 2));
            if (val == 0xFFFF)
                break;

            ushort unicode;
            if (!PWCodeToUnicode.TryGetValue(val, out unicode))
            {
                bytes = bytes[0..(i * 2)];
                break;
            }

            WriteUInt16LittleEndian(bytes.AsSpan(i * 2), unicode);
        }
        return Encoding.Unicode.GetString(bytes);
    }

